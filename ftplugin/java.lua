local jdtls_status_ok, jdtls = pcall(require, "jdtls")
if not jdtls_status_ok then
  vim.notify("jdtls plugin failed!")
  return
end

local capabilities = vim.lsp.protocol.make_client_capabilities()

-- Determine OS
local home = os.getenv("HOME")
if vim.fn.has("mac") == 1 then
  WORKSPACE_PATH = home .. "/.cache/jdtls/workspace/"
  CONFIG = "mac"
elseif vim.fn.has("unix") == 1 then
  WORKSPACE_PATH = home .. "/.cache/jdtls/workspace/"
  CONFIG = "linux"
else
  print("Unsupported system")
end

-- Find root of project
local root_markers = { ".git", "mvnw", "gradlew", "pom.xml", "build.gradle" }
local root_dir = require("jdtls.setup").find_root(root_markers) or vim.fn.getcwd()
if root_dir == "" then
  return
end

